package com.example.madclasstaskassignment;

public class Student {
    private String contact,name;
    private int id;

    public Student() {
        this.contact = contact;
        this.name = name;
        this.id = id;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String rollNumber) {
        this.contact = rollNumber;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
